package com.tanbir.basic;


public class Using_Binaryoperator {
    public static void main(String[] args) {
        int a = 11;
        float b = 55.69f;
        int c = 77;
        double d = 11.25;
        int e = a+c;
        float f = b-a;
        double g = d*2; 
        int h = a/2;
        int j = 100;
        int i = j%33;
        
        System.out.println("The addition of two numbers:" + e);
        System.out.println("The subtraction of two numbers:" + f);
        System.out.println("The multiplication of two numbers:" + g);
        System.out.println("The division of two numbers:" + h);
        System.out.println("The modulas of two numbers:" + i);
        
    }
}
