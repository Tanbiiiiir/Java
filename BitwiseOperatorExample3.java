package com.tanbir.basic;


public class BitwiseOperatorExample3 {
    public static void main(String[] args) {
        int a = 6;
        int b = 7;
        int and = a & b;
        int or = a | b;
        int rightShift = 5 >> 2;
        
        System.out.println("A & B = " + and);
        System.out.println("A | B =" + or);
        System.out.println("5 >> 2 = " + rightShift);
    }
    
}
