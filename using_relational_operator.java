public class using_relational_operator {
    
    public static void main(String[] args) {
        int mark = 111;
        
        if (mark > 100){
            System.out.println("The number is invalid.");
        }
        
        else if(mark == 100){
            System.out.println("You got full mark.");
        }
        
        else if(mark >= 80){
            System.out.println("A+");
        }
        
        else if(mark >= 70){
            System.out.println("A");
        }
        
        else if(mark >= 60){
            System.out.println("B+");
        }
        
        
        
    }
}
