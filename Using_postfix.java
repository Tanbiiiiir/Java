package com.tanbir.basic;

public class Using_postfix {
    public static void main(String[] args) {
        
        int x = 45;
        double y = 77.2;
        float z = 96.69f;
        System.out.println(x++);
        System.out.println(x);
        
        System.out.println(y--);
        System.out.println(y);
        
        System.out.println(z++);
        System.out.println(z);
        
    }
}
