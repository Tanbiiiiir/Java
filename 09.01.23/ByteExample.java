package com.tanbir.basic;


public class ByteExample {
    public static void main(String[] args) {
        byte value = 30;
        System.out.println("Enter the value: " + value);
        
        Short s = -26873;
        System.out.println("Short result is: " + s);
        
        int a = 111;
        System.out.println("Int result is: " + a);
        
        double d = 121.12123;
        System.out.println("Double result is: " + d);
        
        float f = 23.77f;
        System.out.println("Float result is: " + f);
        
        long l = 8736582278364587l;
        System.out.println("Long result is: " + l);
    }
    
}
