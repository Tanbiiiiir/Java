package com.tanbir.basic;


public class Using_prefix {
    public static void main(String[] args) {
            
    int a = 69;
    int b = 96;
    System.out.println("The value of A:"+ ++a); //using ++a means increment by 1
    System.out.println("The value of A:"+ a);
    
    
    System.out.println("The value of B:"+ --b); //ussing --a means decriment by 1
    System.out.println("The value of B:"+ --b);
    
    
    
     
    }
       
   
}

